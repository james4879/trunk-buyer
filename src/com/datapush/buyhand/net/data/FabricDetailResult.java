package com.datapush.buyhand.net.data;

import java.util.ArrayList;
import java.util.List;

public class FabricDetailResult {

	private String userName;

	private String title;

	private String publishedTime;

	private String longPublishedTime;

	private String content;

	private int commment;

	private String headPic;

	private String collectCount;

	private Collect collect;

	public String getCollectCount() {
		return collectCount;
	}

	public void setCollectCount(String collectCount) {
		this.collectCount = collectCount;
	}

	private List<BuyerTopicImageForm> imageList = new ArrayList<BuyerTopicImageForm>();

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPublishedTime() {
		return publishedTime;
	}

	public void setPublishedTime(String publishedTime) {
		this.publishedTime = publishedTime;
	}

	public String getLongPublishedTime() {
		return longPublishedTime;
	}

	public void setLongPublishedTime(String longPublishedTime) {
		this.longPublishedTime = longPublishedTime;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getCommment() {
		return commment;
	}

	public void setCommment(int commment) {
		this.commment = commment;
	}

	public String getHeadPic() {
		return headPic;
	}

	public void setHeadPic(String headPic) {
		this.headPic = headPic;
	}

	public List<BuyerTopicImageForm> getImageList() {
		return imageList;
	}

	public void setImageList(List<BuyerTopicImageForm> imageList) {
		this.imageList = imageList;
	}

	public Collect getCollect() {
		return collect;
	}

	public void setCollect(Collect collect) {
		this.collect = collect;
	}

}
